import Foundation

/// Semantic color rules used throughout the app.
/// These are the ONLY colors that UI/feature code should reference.


public enum SemanticColor {
 // Text colors
 case textPrimary
 case textSecondary
 case textMain
 
 // Background colors
 case appBackground
 case buttonBackground
 
 // Border colors
 case borderPrimary
 case borderError
 case borderWarning

}
